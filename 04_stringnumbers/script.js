// Increment (++)
// The increment (++) operator increments (adds one to) its operand and returns the value before or after the increment, depending on where the operator is placed.


let value1 = 3;
const value2 = value1++;
console.log(value2);

//in post fix new variable take value before incrementing 
//for eg. in this value1=4,value2=3.


let value = 3;
const value3 = ++value;
console.log(value3);

//in prefix new variable take/assign value after value is incremented
//in this eg. value=3, value3=4.

